// NumberCheckUsingIfElse.java
// This program checks whether a number is positive, negative, or zero using if-else statements

public class NumberCheckUsingIfElse {
    public static void main(String[] args) {
        int number = 10;  // Change this value to test different numbers

        if (number > 0) {
            System.out.println(number + " is positive.");
        } else if (number < 0) {
            System.out.println(number + " is negative.");
        } else {
            System.out.println("The number is zero.");
        }
    }
}

public class StringConcatenationTutorial {

    public static void main(String[] args) {
        // Example 1: Simple Concatenation
        var name = "Priya";  // Storing a name
        var message = name + " will become a software engineer!";
        System.out.println(message);

        // Separator for clarity in output
        System.out.println();

        // Example 2: Concatenating Multiple Variables
        var firstName = "Rahul";
        var lastName = "Sharma";
        var fullMessage = firstName + " " + lastName + " is learning Java.";
        System.out.println(fullMessage);
    }
}
